/********************************************
 * ЦИКЛЫ В JAVASCRIPT
 ********************************************

 Цикл — это конструкция, которая позволяет выполнять один и тот же участок кода
 несколько раз подряд, пока выполняется заданное условие.

 Циклы применяются для автоматизации повторяющихся действий:
 - проход по элементам массива;
 - выполнение кода до наступления определённого события;
 - накопление, подсчёт, фильтрация данных и т.д.

 В JavaScript есть несколько видов циклов:
 1. for
 2. Добавляем только html + css. while
 3. do...while
 4. for...of (для коллекций)
 5. for...in (для объектов)
 ********************************************/


/*---------------------------------------------------
  1. ЦИКЛ FOR
---------------------------------------------------

Используется, когда заранее известно количество повторений.

Синтаксис:
for (инициализация; условие; шаг) {
    // тело цикла
}

- инициализация: создаёт переменную-счётчик (обычно let i = 0)
- условие: проверяется перед каждой итерацией
- шаг: выполняется после каждой итерации
*/

let num = 0

for (let i = 0; i < 5; i++) {
    num += i
    console.log("Итерация №", i);
    console.log("num = ", num);
}

console.log("Res num = ", num);


/*
Разбор:
1. i = 0            → начальное значение счётчика
2. Добавляем только html + css. i < 5            → пока условие истинно, тело цикла выполняется
3. i++              → после каждой итерации счётчик увеличивается на 1

Результат:
Будут выведены числа от 0 до 4.
*/


/*---------------------------------------------------
  2. Добавляем только html + css. ЦИКЛ WHILE
---------------------------------------------------

Используется, когда количество повторений заранее неизвестно.
Условие проверяется ПЕРЕД каждой итерацией.

Синтаксис:
while (условие) {
    // тело цикла
}
*/

let count = 0;

while (count < 3) {
    console.log("count =", count);
    count++;
}

/*
Разбор:
1. Проверяется условие (count < 3).
2. Добавляем только html + css. Если true → выполняется тело цикла.
3. После каждой итерации счётчик увеличивается.
4. Когда count станет равен 3, условие станет false, цикл завершится.
*/


/*---------------------------------------------------
  3. ЦИКЛ DO...WHILE
---------------------------------------------------

Отличие от while: тело цикла выполняется хотя бы один раз,
даже если условие изначально ложно.

Синтаксис:
do {
    // тело цикла
} while (условие);
*/

let num = 5;

do {
    console.log("Это выполняется хотя бы один раз. num =", num);
    num++;
} while (num < 5);

/*
Здесь условие (num < 5) изначально ложно, но тело цикла всё равно
выполнится один раз перед проверкой.
*/


/*---------------------------------------------------
  4. ЦИКЛ FOR...OF (для массивов и коллекций)
---------------------------------------------------

Используется для перебора значений внутри массива, строки или другой итерируемой структуры.

Синтаксис:
for (переменная of коллекция) {
    // действия с элементом
}
*/

let fruits = ["яблоко", "банан", "вишня"];

for (let fruit of fruits) {
    console.log("Фрукт:", fruit);
}

/*
Цикл for...of перебирает значения элементов массива по порядку.
В отличие от обычного for, не требует ручного обращения по индексу.
*/


/*---------------------------------------------------
  5. ЦИКЛ FOR...IN (для объектов)
---------------------------------------------------

Используется для перебора свойств (ключей) объекта.

Синтаксис:
for (ключ in объект) {
    // действия с ключом или значением
}
*/

let car = {
    brand: "Toyota",
    model: "Camry",
    year: 2024
};

for (let key in car) {
    console.log("Свойство:", key, "Значение:", car[key]);
}

/*
Цикл for...in перебирает ключи объекта (brand, model, year).
Чтобы получить значение, используется синтаксис car[key].
*/


/*---------------------------------------------------
  6. ВЛОЖЕННЫЕ ЦИКЛЫ
---------------------------------------------------

Циклы могут быть вложены друг в друга.
Обычно это нужно при работе с многомерными массивами.
*/

let matrix = [
    [1, 2, 3],
    [4, 5, 6]
];

for (let i = 0; i < matrix.length; i++) {
    for (let j = 0; j < matrix[i].length; j++) {
        console.log("Элемент:", matrix[i][j]);
    }
}

/*
Здесь внешний цикл проходит по строкам, а внутренний — по элементам внутри строки.
*/


/*---------------------------------------------------
  7. КОМАНДЫ break и continue
---------------------------------------------------

break — полностью прерывает выполнение цикла.
continue — пропускает текущую итерацию и переходит к следующей.
*/

for (let i = 0; i < 5; i++) {
    if (i === 3) {
        console.log("Останавливаем цикл на i =", i);
        break;
    }
    console.log("Текущее значение i:", i);
}

for (let i = 0; i < 5; i++) {
    if (i === 2) {
        console.log("Пропускаем итерацию i =", i);
        continue;
    }
    console.log("Итерация с i =", i);
}

/*
break используется, когда нужно досрочно выйти из цикла.
continue — когда нужно пропустить только одну итерацию.
*/


/*---------------------------------------------------
  8. СРАВНЕНИЕ ОСНОВНЫХ ЦИКЛОВ
---------------------------------------------------

| Тип цикла   | Когда использовать                                  |
|--------------|----------------------------------------------------|
| for          | Когда известно количество повторений               |
| while        | Когда неизвестно, сколько раз нужно повторить      |
| do...while   | Когда нужно выполнить хотя бы один раз             |
| for...of     | Для перебора элементов массива или строки          |
| for...in     | Для перебора свойств объекта                       |

*/


/*---------------------------------------------------
  9. ПОДВЕДЕНИЕ ИТОГОВ
---------------------------------------------------

Основные термины:
- Итерация — одно повторение цикла.
- Счётчик — переменная, которая контролирует количество итераций.
- Условие — логическое выражение, определяющее, должен ли цикл продолжаться.
- Тело цикла — код, выполняемый при каждой итерации.

Циклы — это фундаментальный инструмент в программировании.
Они позволяют избежать дублирования кода и автоматизировать
повторяющиеся действия, делая программы компактными и эффективными.
---------------------------------------------------*/
