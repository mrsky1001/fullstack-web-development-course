/*---------------------------------------------------
  1. Объекты в JavaScript
---------------------------------------------------
Объект — это коллекция «ключ — значение».
Ключи — строки, значения могут быть любыми типами данных.
Используются для хранения связанных свойств сущности.
*/

let person = {
    name: "Иван",         // имя
    age: 25,              // возраст
    isStudent: true,      // логический флаг
    hello: function() {   // метод объекта (функция внутри объекта)
        console.log("Привет! Меня зовут " + this.name);
    }
};

console.log("Имя:", person.name); // доступ к свойству через точку
person.hello(); // вызов метода объекта

// Добавление нового свойства в объект:
person.country = "Россия";
console.log("Страна:", person.country);

// Теория:
// - Объекты позволяют объединять данные и методы
// - this внутри метода объекта ссылается на текущий объект
// - Свойства можно изменять и добавлять динамически

/*---------------------------------------------------
  2. Массивы в JavaScript
---------------------------------------------------
Массив — это упорядоченный набор элементов.
Элементы хранятся по индексам (начиная с 0).
Массивы удобны для работы со списками данных, циклов и перебора.
*/

let fruits = ["Яблоко", "Банан", "Апельсин"];
console.log("Первый элемент:", fruits[0]); // доступ по индексу

// Добавление элемента в конец массива
fruits.push("Груша");
console.log("После push:", fruits);

// Изменение элемента
fruits[1] = "Киви";
console.log("После изменения:", fruits);

// Длина массива
console.log("Длина массива:", fruits.length);

// Теория:
// - Массивы могут хранить любые типы данных, включая объекты и другие массивы
// - Индексы начинаются с 0
// - Часто используются циклы для перебора элементов

/*---------------------------------------------------
  3. Условные операторы (if, else, else if)
---------------------------------------------------
Используются для принятия решений в программе.
if — выполняет блок кода, если условие true
else if — проверяет альтернативное условие
else — выполняется, если все условия false
*/

let age = 18;

if (age > 18) {
    console.log("Вы старше 18");
} else if (age === 18) {
    console.log("Вам ровно 18");
} else {
    console.log("Вы младше 18");
}

// Теория:
// - Условные операторы позволяют «ветвить» выполнение программы
// - Вложенные if конструкции позволяют проверять несколько условий

/*---------------------------------------------------
  4. Тернарный оператор
---------------------------------------------------
Короткая запись для if...else
Условие ? значение_если_true : значение_если_false
*/

let access = (age >= 18) ? "Доступ разрешён" : "Доступ запрещён";
console.log(access);

// Теория:
// - Тернарный оператор удобен для простых условий
// - Можно использовать в присваивании переменных или выводе на экран

/*---------------------------------------------------
  5. Оператор switch
---------------------------------------------------
Используется для множественного выбора.
switch сравнивает одно выражение с множеством значений.
*/

let day = 3;

switch (day) {
    case 1:
        console.log("Понедельник");
        break;
    case 2:
        console.log("Вторник");
        break;
    case 3:
        console.log("Среда");
        break;
    default:
        console.log("Другой день");
}

// Теория:
// - break обязателен, чтобы не выполнялись последующие кейсы
// - default выполняется, если ни один case не подошёл
// - switch удобен при множестве проверок на равенство

/*---------------------------------------------------
  6. Циклы for, while и do...while
---------------------------------------------------
Циклы позволяют повторять блоки кода несколько раз

1) for — используется, когда известно количество повторений
*/

for (let i = 0; i < 5; i++) {
    console.log("for i =", i);
}

/*
Теория:
- Инициализация: let i = 0
- Условие: i < 5
- Инкремент: i++
- Блок кода выполняется, пока условие true
*/

/* 2) while — выполняется, пока условие true */

let i = 0;
while (i < 3) {
    console.log("while i =", i);
    i++;
}

/* 3) do...while — выполняется хотя бы один раз, затем проверяет условие */

let j = 0;
do {
    console.log("do...while j =", j);
    j++;
} while (j < 2);

// Теория циклов:
// - for удобен для массивов и фиксированного числа повторов
// - while используется, если количество итераций заранее неизвестно
// - do...while гарантирует хотя бы один запуск блока кода
