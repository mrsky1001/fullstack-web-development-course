/********************************************
 * УРОК: Основы массивов в JavaScript
 ********************************************

 Что такое массив?

 Массив (Array) — это **упорядоченная коллекция данных**, где
 каждое значение (элемент) хранится под своим индексом (позицией).

 Терминология:
 - Элемент массива — отдельное значение внутри коллекции.
 - Индекс — порядковый номер элемента, начинается с 0.
 - Длина массива (length) — количество элементов.
 - Итерируемый объект — структура, по которой можно проходить циклом.

 Примеры использования массивов:
 - хранение списка товаров;
 - списка студентов;
 - результатов запросов API;
 - координат, данных, событий и т.д.

 ********************************************/


/*---------------------------------------------------
  1. Создание массива
---------------------------------------------------*/

//  Способ 1: Литерал массива []
const fruits = ["яблоко", "банан", "киви"];
console.log(fruits);

//  Способ 2: Через конструктор new Array()
const numbers = new Array(10, 20, 30, 40);
console.log(numbers);

//  Пустой массив
const empty = [];
console.log(empty);

/*
 Литерал массива — предпочтительный способ.
Конструктор new Array() обычно не используется, чтобы избежать путаницы.
*/


/*---------------------------------------------------
  2. Индексация и доступ к элементам
---------------------------------------------------*/

console.log("Первый элемент:", fruits[0]);  // яблоко
console.log("Последний элемент:", fruits[fruits.length - 1]); // киви

// Изменение значения по индексу
fruits[1] = "апельсин";
console.log("Обновлённый массив:", fruits);

/*
 Индексы начинаются с 0.
Доступ к элементу осуществляется по его позиции: array[index].
*/

/*--------------------------------------------------- */

/* Что такое индекс?

Индекс — это порядковый номер элемента в массиве.
    Он указывает, где именно хранится элемент внутри коллекции.

Важно:
    - Нумерация начинается с 0 (нулевой индекс).
- Каждый элемент имеет **уникальный индекс**.
- Индекс используется для чтения, изменения и удаления данных.

********************************************/


/*---------------------------------------------------
  1. Пример массива и его индексов
---------------------------------------------------*/

/*
Визуальная схема:

Индексы:   0        1        2        3
            ↓        ↓        ↓        ↓
Массив:  ["яблоко", "банан", "киви", "груша"]

То есть:
fruits[0] → "яблоко"
fruits[1] → "банан"
fruits[2] → "киви"
fruits[3] → "груша"
*/


/*---------------------------------------------------
  3. Длина массива (свойство length)
---------------------------------------------------*/

console.log("Длина массива fruits:", fruits.length);

// Можно изменить длину вручную
fruits.length = 2; // усечёт массив
console.log("После усечения:", fruits);

/*
 Свойство length показывает количество элементов.
Если уменьшить его вручную — элементы "обрежутся".
*/


/*---------------------------------------------------
  4. Добавление и удаление элементов
---------------------------------------------------*/

const animals = ["кот", "пёс", "попугай"];

// Добавить в конец — push()
animals.push("черепаха");
console.log("После push:", animals);

// Удалить из конца — pop()
animals.pop();
console.log("После pop:", animals);

// Добавить в начало — unshift()
animals.unshift("тигр");
console.log("После unshift:", animals);

// Удалить из начала — shift()
animals.shift();
console.log("После shift:", animals);

/*
 Методы push/pop работают с "хвостом" массива.
 Методы unshift/shift — с "началом".
*/

/*---------------------------------------------------
  Добавить элемент в середину
  Метод splice()
  ---------------------------------------------------

Синтаксис:
array.splice(начальный_индекс, сколько_удалить, элементы_для_вставки)

Описание параметров:
- начальный_индекс — куда вставлять элемент
- сколько_удалить — сколько удалить элементов (0 = ничего не удалять)
- элементы_для_вставки — что вставить на это место
*/

// Пример массива:
const colors = ["красный", "зелёный", "синий", "жёлтый"];

console.log("Исходный массив:", colors);

// Вставим элемент "оранжевый" на позицию между "зелёный" и "синий"
colors.splice(2, 0, "оранжевый");
/*
  Пояснение:
  2 → индекс, куда вставляем
  0 → не удаляем ничего
  "оранжевый" → элемент для вставки
*/

console.log("После вставки:", colors);
// ["красный", "зелёный", "оранжевый", "синий", "жёлтый"]

/*---------------------------------------------------
  5. Перебор массива
---------------------------------------------------*/

const colors = ["красный", "зелёный", "синий"];

// Классический for
for (let i = 0; i < colors.length; i++) {
    console.log(`Цвет №${i + 1}: ${colors[i]}`);
}

// Современный for...of
for (let color of colors) {
    console.log("Цвет:", color);
}

/*
 for — позволяет контролировать индексы.
 for...of — читабельный способ пройтись по элементам.
*/


/*---------------------------------------------------
  6. Метод forEach()
---------------------------------------------------*/

colors.forEach(function(item, index) {
    console.log(`Элемент ${index}: ${item}`);
});

/*
 forEach() выполняет функцию для каждого элемента массива.
- item — значение элемента
- index — его индекс
*/


/*---------------------------------------------------
  7. Методы поиска элементов
---------------------------------------------------*/

const nums = [10, 20, 30, 40, 50];

console.log("Индекс 30:", nums.indexOf(30)); // 2
console.log("Содержит 100?", nums.includes(100)); // false
console.log("Найти > 25:", nums.find(n => n > 25)); // 30
console.log("Индекс > 25:", nums.findIndex(n => n > 25)); // 2

/*
 Методы поиска:
- indexOf() — возвращает индекс элемента
- includes() — проверяет наличие
- find() — возвращает первый элемент, подходящий по условию
- findIndex() — возвращает его индекс
*/


/*---------------------------------------------------
  8. Методы сортировки и преобразования
---------------------------------------------------*/

const letters = ["b", "a", "d", "c"];
letters.sort();
console.log("После sort:", letters); // ["a", "b", "c", "d"]

letters.reverse();
console.log("После reverse:", letters); // ["d", "c", "b", "a"]

/*
 sort() — сортирует элементы по алфавиту (по умолчанию — как строки).
 reverse() — меняет порядок элементов на противоположный.
*/


/*---------------------------------------------------
  9. Методы фильтрации и преобразования
---------------------------------------------------*/

const numbers2 = [1, 2, 3, 4, 5, 6];

// filter() — отбирает элементы по условию
const even = numbers2.filter(num => num % 2 === 0);
console.log("Чётные:", even);

// map() — создаёт новый массив, применяя функцию к каждому элементу
const squares = numbers2.map(num => num ** 2);
console.log("Квадраты:", squares);

// reduce() — сворачивает массив в одно значение
const sum = numbers2.reduce((acc, val) => acc + val, 0);
console.log("Сумма элементов:", sum);

/*
 Методы высшего порядка:
- filter() — фильтрация по условию.
- map() — преобразование каждого элемента.
- reduce() — объединение в одно итоговое значение.
*/


/*---------------------------------------------------
  10. Объединение и извлечение частей массива
---------------------------------------------------*/

const arr1 = [1, 2];
const arr2 = [3, 4];

// concat() — объединяет массивы
const merged = arr1.concat(arr2);
console.log("Объединённый массив:", merged);

// slice() — извлекает часть массива
const part = merged.slice(1, 3);
console.log("Часть массива:", part);

// splice() — изменяет массив: добавление/удаление
merged.splice(2, 1, "новый элемент");
console.log("После splice:", merged);

/*
 concat() — создаёт новый массив из нескольких.
 slice() — возвращает копию части массива (не изменяет оригинал).
 splice() — изменяет оригинальный массив.
*/


/*---------------------------------------------------
  11. Spread оператор и деструктуризация
---------------------------------------------------*/

const numsA = [1, 2, 3];
const numsB = [4, 5, 6];

// spread (...) — разворачивает массив
const combined = [...numsA, ...numsB];
console.log("Объединено через spread:", combined);

// деструктуризация — извлечение значений
const [first, second, ...rest] = combined;
console.log("Первый:", first);
console.log("Второй:", second);
console.log("Остальные:", rest);

/*
 Spread (...) — «разворачивает» элементы массива.
 Деструктуризация — позволяет легко извлекать значения из массива.
*/


/*---------------------------------------------------
  12. Многомерные массивы (вложенные)
---------------------------------------------------*/

const matrix = [
    [1, 2, 3],
    [4, 5, 6],
    [7, 8, 9]
];

console.log("Элемент [1][2]:", matrix[1][2]); // 6

/*
 Многомерные массивы используются для таблиц, координат, сеток.
matrix[row][column]
*/


/*---------------------------------------------------
  13. Массивы и объекты: различие
---------------------------------------------------

 Объект (Object):
  - хранит данные в виде пар "ключ: значение"
  - порядок свойств не гарантирован

 Массив (Array):
  - хранит данные по индексам (порядок важен)
  - имеет числовую индексацию и встроенные методы

---------------------------------------------------*/


/*---------------------------------------------------
  14. Итог
---------------------------------------------------

 Ключевые понятия:

- Массив — это коллекция элементов, индексированных числами.
- Индексы начинаются с 0.
- Свойство length определяет длину.
- Методы push/pop, shift/unshift — для добавления и удаления.
- Методы map, filter, reduce — для обработки данных.
- Spread и деструктуризация делают работу с массивами гибче.
- Массив — основа для работы с данными и API в JavaScript.

---------------------------------------------------*/
