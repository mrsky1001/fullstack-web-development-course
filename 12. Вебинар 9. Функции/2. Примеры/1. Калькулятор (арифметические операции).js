/*
Цель:
  Научиться создавать функции для выполнения базовых математических операций.
  Функции принимают аргументы (входные данные) и возвращают результат.
*/

// Функции для основных операций
function add(a, b) {
    return a + b; // возвращаем сумму
}

function subtract(a, b) {
    return a - b; // возвращаем разность
}

function multiply(a, b) {
    return a * b; // возвращаем произведение
}

function divide(a, b) {
    if (b === 0) {
        return "Ошибка: деление на ноль невозможно!";
    }
    return a / b; // возвращаем результат деления
}

// Пример использования
let x = 12;
let y = 0;

console.log("Сложение:", add(x, y));
console.log("Вычитание:", subtract(x, y));
console.log("Умножение:", multiply(x, y));
console.log("Деление:", divide(x, y));
