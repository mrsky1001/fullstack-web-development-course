/*
Math.random() — возвращает случайное число от 0 до 1.
Math.floor() — округляет вниз до ближайшего целого.

Программа генерирует число от 1 до 10.
Пользователь вводит догадку, и программа проверяет результат.
*/

function guessNumber(userGuess) {
    const secret = Math.floor(Math.random() * 10) + 1;

    console.log("Загаданное число:", secret);
    console.log("Ваше предположение:", userGuess);

    if (userGuess === secret) {
        console.log("Поздравляем! Вы угадали число!");
    } else {
        console.log("Неверно! Попробуйте снова.");
    }
}

// Пример использования:
guessNumber(7);
