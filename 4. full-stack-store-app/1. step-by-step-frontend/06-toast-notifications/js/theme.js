            // –ü—Ä–∏–º–µ–Ω—è–µ–º –Ω–æ–≤—É—é —Ç–µ–º—É
            html.setAttribute('data-theme', newTheme);

            // –°–æ—Ö—Ä–∞–Ω—è–µ–º –≤—ã–±–æ—Ä –≤ localStorage
            // localStorage —Ö—Ä–∞–Ω–∏—Ç –¥–∞–Ω–Ω—ã–µ –º–µ–∂–¥—É —Å–µ—Å—Å–∏—è–º–∏ –±—Ä–∞—É–∑–µ—Ä–∞
