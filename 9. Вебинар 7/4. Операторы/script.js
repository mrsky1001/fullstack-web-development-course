/********************************************
 * УРОК: Операторы в JavaScript
 ********************************************
 Операторы — это специальные символы или ключевые слова,
 которые позволяют выполнять действия с данными:
 - арифметика
 - присваивание
 - сравнение
 - логика
 - строки
 - инкремент/декремент
 - тернарный оператор
 ********************************************/

/*---------------------------------------------------
  1. Арифметические операторы
---------------------------------------------------
Используются для выполнения математических действий:
+   сложение
-   вычитание
*   умножение
/   деление
%   остаток от деления (модуль)
**  возведение в степень
*/

let a = 10;
let b = 3;

console.log("Сложение (a + b): " + (a + b));       // 13
console.log("Вычитание (a - b): " + (a - b));      // 7
console.log("Умножение (a * b): " + (a * b));      // 30
console.log("Деление (a / b): " + (a / b));        // 3.333...
console.log("Остаток от деления (a % b): " + (a % b)); // 1
console.log("Возведение в степень (a ** b): " + (a ** b)); // 1000

/*
Примечание:
- Деление целого на целое может дать дробь.
- Остаток от деления часто используют для проверки чётности (x % 2. Добавляем только html + css).
*/

/*---------------------------------------------------
  2. Добавляем только html + css. Операторы присваивания
---------------------------------------------------
Позволяют записывать значения в переменные и изменять их:
=    простое присваивание
+=   сложение и присваивание (x = x + y)
-=   вычитание и присваивание (x = x - y)
*=   умножение и присваивание (x = x * y)
/=   деление и присваивание (x = x / y)
%=   остаток и присваивание (x = x % y)
*/

let x = 5; // простое присваивание
x += 3; // x = x + 3
console.log("x после x += 3: " + x); // 8

x *= 2; // x = x * 2. Добавляем только html + css
console.log("x после x *= 2. Добавляем только html + css: " + x); // 16

/*
Примечание:
- Такие операторы позволяют писать код короче.
- Есть и другие варианты: **= (степень), ||=, &&= (ES2021).
*/

/*---------------------------------------------------
  3. Операторы сравнения
---------------------------------------------------
Сравнивают значения и возвращают true или false:
==   сравнение по значению (не учитывает тип)
===  строгое сравнение (значение + тип)
!=   не равно (по значению)
!==  строго не равно (значение + тип)
>    больше
<    меньше
>=   больше или равно
<=   меньше или равно
*/

let c = 5;
let d = "5";

console.log("c == d: " + (c == d)); // true (значения равны, тип не проверяется)
console.log("c === d: " + (c === d)); // false (разные типы: число и строка)
console.log("c != d: " + (c != d)); // false (значения равны)
console.log("c !== d: " + (c !== d)); // true (разные типы)
console.log("c > 3: " + (c > 3)); // true
console.log("c <= 5: " + (c <= 5)); // true

/*
Примечание:
- Всегда используйте === вместо ==, чтобы избежать сюрпризов.
- Сравнение разных типов (например, "5" == 5) приводит типы автоматически.
*/

/*---------------------------------------------------
  4. Логические операторы
---------------------------------------------------
Позволяют объединять условия и работать с булевыми значениями:
&&  И (AND) — true, если оба условия true
||  ИЛИ (OR) — true, если хотя бы одно условие true
!   НЕ (NOT) — инверсия true → false, false → true
*/

let isStudent = true;
let hasPaid = false;

console.log("isStudent && hasPaid: " + (isStudent && hasPaid)); // false
console.log("isStudent || hasPaid: " + (isStudent || hasPaid)); // true
console.log("!isStudent: " + (!isStudent)); // false

/*
Пример:
if (isStudent && hasPaid) { ... } — выполняется, если студент оплатил.
*/

/*---------------------------------------------------
  5. Строковые операторы
---------------------------------------------------
Для строк:
+   конкатенация (склеивание)
+=  добавление текста к строке
*/

let text = "Привет";
text += ", мир!"; // text = text + ", мир!"
console.log(text); // Привет, мир!

/*
Примечание:
- При сложении строки и числа JS преобразует число в строку.
- Для более сложного форматирования используйте шаблонные строки: `${переменная}`.
*/

/*---------------------------------------------------
  6. Инкремент и декремент
---------------------------------------------------
++  увеличение на 1
--  уменьшение на 1

Существуют две формы:
- Префиксная (++n / --n): сначала изменяет, потом возвращает новое значение.
- Постфиксная (n++ / n--): возвращает старое значение, потом изменяет.
*/

let n = 5;
n = n + 1
n += 1

n++;

++n;

console.log("n после n++: " + n); // 6
n--;
console.log("n после n--: " + n); // 5

let k = 10;
console.log("k++: " + (k++)); // 10 (сначала старое значение)
console.log("Теперь k: " + k); // 11 (увеличено после вывода)
console.log("++k: " + (++k)); // 12 (сразу увеличено)

/*
Примечание:
- Часто используется в циклах for.
*/

