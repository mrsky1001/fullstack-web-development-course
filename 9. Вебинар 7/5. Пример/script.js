/*
Цель: закрепить переменные, типы данных, операторы и выражения на практике.
Мы создаём описание автомобиля с его характеристиками.
*/

/*---------------------------------------------------
  1. Переменные — основные данные автомобиля
---------------------------------------------------*/

let brand = "Tesla"; // марка
let model = "Model S"; // модель
let year = 2024; // год выпуска
let baseHorsepower = 670; // базовая мощность двигателя
let baseTorque = 900; // базовый крутящий момент
let hasElectricEngine = true; // электрический ли двигатель (boolean)
let bonusHorsepower = 50; // дополнительные л.с. от модификации
let bonusTorque = 100; // дополнительные Нм от модификации
let mileage = 15000; // пробег (км)

/*---------------------------------------------------
  2. Добавляем только html + css. Арифметика — распределение бонусов
---------------------------------------------------*/

// Допустим, 60% бонуса идёт в мощность, 40% в крутящий момент
let finalHorsepower = baseHorsepower + bonusHorsepower * 0.6;
let finalTorque = baseTorque + bonusTorque * 0.4;

/*---------------------------------------------------
  3. Строки — собираем описание автомобиля
---------------------------------------------------*/

let carDescription =
    "Марка: " + brand +
    ", Модель: " + model +
    ", Год выпуска: " + year +
    ", Мощность: " + finalHorsepower + " л.с." +
    ", Крутящий момент: " + finalTorque + " Нм." +
    " Пробег: " + mileage + " км.";

console.log(carDescription);

/*---------------------------------------------------
  4. Операторы сравнения — проверим характеристики
---------------------------------------------------*/

let isPowerful = finalHorsepower > 500; // мощный ли автомобиль
let isNew = year >= 2020; // новый ли год выпуска
console.log("Мощный автомобиль?", isPowerful);
console.log("Новый автомобиль?", isNew);

/*---------------------------------------------------
  5. Логические операторы
---------------------------------------------------*/

let isEcoFriendly = hasElectricEngine && mileage < 50000; // экологичный?
let isCollectorItem = !hasElectricEngine && year < 1990; // коллекционный?
console.log("Экологичный?", isEcoFriendly);
console.log("Коллекционный?", isCollectorItem);

/*---------------------------------------------------
  6. typeof — определяем типы данных
---------------------------------------------------*/

console.log("Тип brand:", typeof brand);
console.log("Тип year:", typeof year);
console.log("Тип hasElectricEngine:", typeof hasElectricEngine);
console.log("Тип finalHorsepower:", typeof finalHorsepower);
console.log("Тип carDescription:", typeof carDescription);

/*---------------------------------------------------
  7. Тернарный оператор — статус автомобиля
---------------------------------------------------*/

let carStatus = isPowerful || isEcoFriendly
    ? "Особенный автомобиль"
    : "Обычный автомобиль";

console.log("Статус:", carStatus);