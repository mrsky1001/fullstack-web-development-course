/********************************************
  Переменная — это "контейнер" для хранения данных.
  В JavaScript переменные создаются с помощью ключевых слов:
  - var (старый способ, сейчас почти не используется)
  - let (современный способ)
  - const (для констант — значений, которые нельзя переопределять)
 *********************************************/

/*---------------------------------------------------
  1. Объявление переменной с помощью var
---------------------------------------------------*/

// var — старый способ объявления переменных.
// Использовался до появления ES6 (2015).
// var имеет "функциональную область видимости" (function scope)
// и подвержен "поднятию" (hoisting), поэтому его сейчас почти не используют.

var oldVariable = "Я переменная, созданная через var";
console.log(oldVariable); // Выведет: Я переменная, созданная через var

// Переменной, объявленной через var, можно присвоить другое значение:
oldVariable = "Новое значение var-переменной";
console.log(oldVariable); // Выведет: Новое значение var-переменной


/*---------------------------------------------------
  2. Объявление переменной с помощью let
---------------------------------------------------*/

// let — современный способ объявления переменных.
// Переменные let имеют блочную область видимости (block scope).
// Это значит, что переменная существует только внутри фигурных скобок,
// если она объявлена там.

let userName = "Андрей"; // создаем переменную userName
console.log(userName); // Выведет: Андрей

// Мы можем изменить значение переменной, объявленной через let:
userName = "Марина";
console.log(userName); // Выведет: Марина

// Пример области видимости:
{
    let insideBlock = "Я видна только внутри блока";
    console.log(insideBlock); // Работает
}

// console.log(insideBlock); // Ошибка! Переменная недоступна вне блока


/*---------------------------------------------------
  3. Объявление переменной с помощью const
---------------------------------------------------*/

// const — для константных значений, которые нельзя переопределить.
// Если вы попробуете присвоить новое значение — будет ошибка.

const birthYear = 2000; // создаем константу
console.log(birthYear); // Выведет: 2000

 // birthYear = 1999; // Ошибка! Нельзя переопределить значение константы

/*
  Важно: константа const не делает сам объект неизменяемым,
  она делает неизменной только ссылку на объект.

  Например:
*/
const user = {
    name: "Иван",
    age: 25
};

// Это допустимо — меняем внутреннее свойство объекта:
user.age = 26;
console.log(user); // {name: "Иван", age: 26}

// Но так нельзя — попытка переназначить сам объект вызовет ошибку:
// user = {}; // Ошибка! Нельзя переназначить константу


/*---------------------------------------------------
  4. Краткая сводка
---------------------------------------------------

- var — старый способ. Использовать только в старых проектах.
- let — для переменных, которые могут изменяться.
- const — для констант (значение нельзя переопределить).

Совет: всегда по умолчанию используйте const,
и только если значение нужно менять — let.
*/

/*---------------------------------------------------
  5. Примеры использования переменных
---------------------------------------------------*/

// Пример 1: Считаем площадь прямоугольника
let width = 10; // ширина
let height = 5; // высота
let area = width * height; // площадь
console.log("Площадь прямоугольника:", area); // 50

// Пример 2: Приветствие
const greeting = "Привет";
let name = "Ольга";
console.log(greeting + ", " + name + "!"); // Привет, Ольга!

// Пример 3: Вычисление возраста
const currentYear = 2025;
let yearOfBirth = 2004;
let age = currentYear - yearOfBirth;
console.log("Ваш возраст:", age); // Ваш возраст: 21
