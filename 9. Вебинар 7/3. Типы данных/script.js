/********************************************
 * УРОК: Типы данных в JavaScript
 ********************************************
 JavaScript — язык с динамической типизацией.
 Тип данных переменной определяется автоматически
 по присвоенному значению, а не жёстко заранее.
 В JS есть 8 основных типов данных:
 - Number
 - String
 - Boolean
 - Undefined
 - Null
 - BigInt
 - Symbol
 - Object (непримитивный)
 ********************************************/

/*---------------------------------------------------
  1. Числа (Number)
---------------------------------------------------
В JS все обычные числа (целые и дробные) относятся к типу Number.
Диапазон: ~ ±1.79e308.
Специальные значения:
  Infinity — бесконечность (например, деление на 0).
  -Infinity — отрицательная бесконечность.
  NaN — «Not-a-Number», результат некорректных операций.
*/

let intNumber = 42; // целое число
let floatNumber = 3.14; // дробное число

console.log("Целое число:", intNumber);
console.log("Дробное число:", floatNumber);

// Деление на ноль даёт Infinity
console.log("Деление на ноль:", 10 / 0); // Infinity

// Попытка умножить строку на число даёт NaN
console.log("Не число (NaN):", "строка" * 2); // NaN

/*---------------------------------------------------
  2. Строки (String)
---------------------------------------------------
Строки — последовательность символов.
Создаются с помощью:
  - Одинарных кавычек ' '
  - Двойных кавычек " "
  - Шаблонных строк ` ` (ES6)
Шаблонные строки позволяют вставлять переменные внутрь
с помощью конструкции ${переменная}.
*/

let single = 'Привет'; // строка в одинарных кавычках
let double = "Мир"; // строка в двойных кавычках
let template = `Шаблонная строка: ${single} ${double}`; // шаблонная строка

console.log(single);
console.log(double);
console.log(template);

// Конкатенация строк (склеивание)
let combined = single + " " + double + "!";
console.log(combined);

/*---------------------------------------------------
  3. Логический тип (Boolean)
---------------------------------------------------
Boolean принимает только два значения:
  true (истина)
  false (ложь)
Используется в условиях, проверках, логике программы.
*/

let isStudent = true; // да, студент
let hasPassed = false; // экзамен не сдан
console.log("Я студент?", isStudent);
console.log("Сдал экзамен?", hasPassed);

/*---------------------------------------------------
  4. undefined
---------------------------------------------------
undefined — специальное значение.
Означает, что переменной ещё не присвоено значение.
Автоматически присваивается, если мы объявили переменную,
но ничего в неё не записали.
*/

let notAssigned; // переменная объявлена, но без значения
console.log("Переменная notAssigned:", notAssigned);

/*---------------------------------------------------
  5. null
---------------------------------------------------
null — отдельный примитивный тип.
Обозначает «пустое значение» или «ничего».
Часто используется, чтобы явно указать «пусто».
*/

let emptyValue = null; // намеренно пустое значение
console.log("Переменная emptyValue:", emptyValue);

/*---------------------------------------------------
  6. BigInt
---------------------------------------------------
BigInt — тип для очень больших целых чисел.
Появился в ES2020.
Позволяет работать с числами больше, чем Number.MAX_SAFE_INTEGER.
Создаётся добавлением «n» в конце числа.
*/

const bigNumber = 123456789012345678901234567890n;
console.log("Очень большое число BigInt:", bigNumber);

/*---------------------------------------------------
  7. Symbol
---------------------------------------------------
Symbol — уникальный и неизменяемый идентификатор.
Используется как ключи свойств объектов или скрытые значения.
Даже два символа с одинаковым описанием не равны друг другу.
*/

const id1 = Symbol("id");
const id2 = Symbol("id");
console.log("Сравнение символов:", id1 === id2); // false

/*---------------------------------------------------
  8. Приведение типов (Type casting)
---------------------------------------------------
JavaScript автоматически приводит типы в выражениях,
но можно делать это явно:
  Number(value) — в число
  String(value) — в строку
  Boolean(value) — в boolean
*/

let numString = "123"; // строка
let num = Number(numString); // явное преобразование в число
console.log("Строка '123' в число:", num);

let boolToString = String(true); // boolean в строку
console.log("true в строку:", boolToString);

let zero = 0;
let boolZero = Boolean(zero); // 0 — это false при преобразовании в Boolean
console.log("0 в Boolean:", boolZero); // false

/*
Совет:
- undefined → false при Boolean()
- null → false
- Пустая строка "" → false
- Любое непустое значение → true
*/