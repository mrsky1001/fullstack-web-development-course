
// –®–ê–ì 1: –ü–æ–¥–∫–ª—é—á–∞–µ–º –≤—Å—Ç—Ä–æ–µ–Ω–Ω—ã–π –º–æ–¥—É–ª—å 'http'
// --------------------------------------------------------------------
// Node.js –∏–º–µ–µ—Ç –º–Ω–æ–≥–æ –≤—Å—Ç—Ä–æ–µ–Ω–Ω—ã—Ö –º–æ–¥—É–ª–µ–π (http, fs, path –∏ –¥—Ä.)
// –ú–æ–¥—É–ª—å 'http' –ø–æ–∑–≤–æ–ª—è–µ—Ç —Å–æ–∑–¥–∞–≤–∞—Ç—å –≤–µ–±-—Å–µ—Ä–≤–µ—Ä—ã
// –§—É–Ω–∫—Ü–∏—è require() - —Å–ø–æ—Å–æ–± –ø–æ–¥–∫–ª—é—á–µ–Ω–∏—è –º–æ–¥—É–ª–µ–π –≤ Node.js

const http = require('http');

// –®–ê–ì 2: –û–ø—Ä–µ–¥–µ–ª—è–µ–º –Ω–∞—Å—Ç—Ä–æ–π–∫–∏ —Å–µ—Ä–≤–µ—Ä–∞
const HOST = 'localhost';  // localhost = –≤–∞—à –∫–æ–º–ø—å—é—Ç–µ—Ä (127.0.0.1)
const PORT = 3000;         // –ü–æ—Ä—Ç 3000 - —Å—Ç–∞–Ω–¥–∞—Ä—Ç–Ω—ã–π –¥–ª—è —Ä–∞–∑—Ä–∞–±–æ—Ç–∫–∏

// –®–ê–ì 3: –°–æ–∑–¥–∞—ë–º HTTP —Å–µ—Ä–≤–µ—Ä
// --------------------------------------------------------------------
// http.createServer() –ø—Ä–∏–Ω–∏–º–∞–µ—Ç —Ñ—É–Ω–∫—Ü–∏—é-–æ–±—Ä–∞–±–æ—Ç—á–∏–∫ (callback)
// –≠—Ç–∞ —Ñ—É–Ω–∫—Ü–∏—è –≤—ã–∑—ã–≤–∞–µ—Ç—Å—è –ø—Ä–∏ –ö–ê–ñ–î–û–ú –∑–∞–ø—Ä–æ—Å–µ –∫ —Å–µ—Ä–≤–µ—Ä—É
// –ü–∞—Ä–∞–º–µ—Ç—Ä—ã:
// - req (request) - –æ–±—ä–µ–∫—Ç —Å –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏–µ–π –æ –∑–∞–ø—Ä–æ—Å–µ (URL, –º–µ—Ç–æ–¥, –∑–∞–≥–æ–ª–æ–≤–∫–∏)
// - res (response) - –æ–±—ä–µ–∫—Ç –¥–ª—è —Ñ–æ—Ä–º–∏—Ä–æ–≤–∞–Ω–∏—è –æ—Ç–≤–µ—Ç–∞ –∫–ª–∏–µ–Ω—Ç—É

const server = http.createServer((req, res) => {
    // –í—ã–≤–æ–¥–∏–º –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é –æ –∑–∞–ø—Ä–æ—Å–µ –≤ –∫–æ–Ω—Å–æ–ª—å —Å–µ—Ä–≤–µ—Ä–∞
    console.log(`[${new Date().toLocaleTimeString()}] ${req.method} ${req.url}`);

    // –®–ê–ì 3.1: –ü—Ä–æ—Å—Ç–∞—è –º–∞—Ä—à—Ä—É—Ç–∏–∑–∞—Ü–∏—è –ø–æ URL
    // ----------------------------------------------------------------
    // req.url —Å–æ–¥–µ—Ä–∂–∏—Ç –ø—É—Ç—å –∑–∞–ø—Ä–æ—Å–∞ (–Ω–∞–ø—Ä–∏–º–µ—Ä, '/' –∏–ª–∏ '/about')
    // –ú—ã –ø—Ä–æ–≤–µ—Ä—è–µ–º URL –∏ –æ—Ç–≤–µ—á–∞–µ–º –ø–æ-—Ä–∞–∑–Ω–æ–º—É

    if (req.url === '/') {
        // –ì–ª–∞–≤–Ω–∞—è —Å—Ç—Ä–∞–Ω–∏—Ü–∞

        // –£—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º HTTP —Å—Ç–∞—Ç—É—Å –∏ –∑–∞–≥–æ–ª–æ–≤–æ–∫ Content-Type
        res.writeHead(200, { 'Content-Type': 'text/html; charset=utf-8' });

        // –û—Ç–ø—Ä–∞–≤–ª—è–µ–º —Ç–µ–ª–æ –æ—Ç–≤–µ—Ç–∞ –∏ –∑–∞–≤–µ—Ä—à–∞–µ–º –µ–≥–æ
        res.end(`
            <h1>üöÄ –î–æ–±—Ä–æ –ø–æ–∂–∞–ª–æ–≤–∞—Ç—å!</h1>
            <p>–≠—Ç–æ –≤–∞—à –ø–µ—Ä–≤—ã–π Node.js —Å–µ—Ä–≤–µ—Ä!</p>
            <p>–ü–æ–ø—Ä–æ–±—É–π—Ç–µ:</p>
            <ul>
                <li><a href="/about">–û –ø—Ä–æ–µ–∫—Ç–µ</a></li>
                <li><a href="/api/status">API —Å—Ç–∞—Ç—É—Å (JSON)</a></li>
            </ul>
        `);

    } else if (req.url === '/about') {
        // –°—Ç—Ä–∞–Ω–∏—Ü–∞ "–û –ø—Ä–æ–µ–∫—Ç–µ"

        res.writeHead(200, { 'Content-Type': 'text/html; charset=utf-8' });
        res.end(`
            <h1>üìö –û –ø—Ä–æ–µ–∫—Ç–µ</h1>
            <p>–≠—Ç–æ —É—á–µ–±–Ω—ã–π –ø—Ä–æ–µ–∫—Ç –∫—É—Ä—Å–∞ Full-Stack Web Development.</p>
            <p><a href="/">‚Üê –ù–∞ –≥–ª–∞–≤–Ω—É—é</a></p>
        `);

    } else if (req.url === '/api/status') {
        // API endpoint - –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç –¥–∞–Ω–Ω—ã–µ –≤ —Ñ–æ—Ä–º–∞—Ç–µ JSON
        // –≠—Ç–æ –æ—Å–Ω–æ–≤–∞ REST API, –∫–æ—Ç–æ—Ä—É—é –º—ã –∏–∑—É—á–∏–º –ø–æ–¥—Ä–æ–±–Ω–µ–µ

        res.writeHead(200, { 'Content-Type': 'application/json; charset=utf-8' });

        // JSON.stringify() –ø—Ä–µ–≤—Ä–∞—â–∞–µ—Ç –æ–±—ä–µ–∫—Ç JavaScript –≤ —Å—Ç—Ä–æ–∫—É JSON
        res.end(JSON.stringify({
            status: 'ok',
            message: '–°–µ—Ä–≤–µ—Ä —Ä–∞–±–æ—Ç–∞–µ—Ç!',
            timestamp: new Date().toISOString(),
            version: '1.0.0'
        }));

    } else {
        // –ï—Å–ª–∏ URL –Ω–µ –Ω–∞–π–¥–µ–Ω - –≤–æ–∑–≤—Ä–∞—â–∞–µ–º 404 (Not Found)

        res.writeHead(404, { 'Content-Type': 'text/html; charset=utf-8' });
        res.end(`
            <h1>‚ùå –û—à–∏–±–∫–∞ 404</h1>
            <p>–°—Ç—Ä–∞–Ω–∏—Ü–∞ "${req.url}" –Ω–µ –Ω–∞–π–¥–µ–Ω–∞</p>
            <p><a href="/">‚Üê –ù–∞ –≥–ª–∞–≤–Ω—É—é</a></p>
        `);
    }
});

// –®–ê–ì 4: –ó–∞–ø—É—Å–∫–∞–µ–º —Å–µ—Ä–≤–µ—Ä
// --------------------------------------------------------------------
// –ú–µ—Ç–æ–¥ listen() –Ω–∞—á–∏–Ω–∞–µ—Ç "—Å–ª—É—à–∞—Ç—å" —É–∫–∞–∑–∞–Ω–Ω—ã–π –ø–æ—Ä—Ç
// Callback –≤—ã–∑—ã–≤–∞–µ—Ç—Å—è –û–î–ò–ù —Ä–∞–∑, –∫–æ–≥–¥–∞ —Å–µ—Ä–≤–µ—Ä —É—Å–ø–µ—à–Ω–æ –∑–∞–ø—É—Å—Ç–∏–ª—Å—è

server.listen(PORT, HOST, () => {
    console.log('');
    console.log('====================================================');
    console.log('  üéì –£–†–û–ö 1: –ë–ê–ó–û–í–´–ô HTTP –°–ï–†–í–ï–†');
    console.log('====================================================');
    console.log(`  ‚úÖ –°–µ—Ä–≤–µ—Ä —É—Å–ø–µ—à–Ω–æ –∑–∞–ø—É—â–µ–Ω!`);
    console.log(`  üìç –ê–¥—Ä–µ—Å: http://${HOST}:${PORT}`);
    console.log('');
    console.log('  –î–æ—Å—Ç—É–ø–Ω—ã–µ –º–∞—Ä—à—Ä—É—Ç—ã:');
    console.log(`    ‚Ä¢ http://${HOST}:${PORT}/          - –ì–ª–∞–≤–Ω–∞—è`);
    console.log(`    ‚Ä¢ http://${HOST}:${PORT}/about     - –û –ø—Ä–æ–µ–∫—Ç–µ`);
    console.log(`    ‚Ä¢ http://${HOST}:${PORT}/api/status - API (JSON)`);
    console.log('');
    console.log('  –î–ª—è –æ—Å—Ç–∞–Ω–æ–≤–∫–∏: Ctrl + C');
    console.log('====================================================');
    console.log('');
});

// –®–ê–ì 5: –û–±—Ä–∞–±–æ—Ç–∫–∞ –æ—à–∏–±–æ–∫ —Å–µ—Ä–≤–µ—Ä–∞
// --------------------------------------------------------------------
// –°–æ–±—ã—Ç–∏–µ 'error' –≤–æ–∑–Ω–∏–∫–∞–µ—Ç –ø—Ä–∏ –ø—Ä–æ–±–ª–µ–º–∞—Ö (–Ω–∞–ø—Ä–∏–º–µ—Ä, –ø–æ—Ä—Ç –∑–∞–Ω—è—Ç)

server.on('error', (err) => {
    if (err.code === 'EADDRINUSE') {
        console.error(`‚ùå –û—à–∏–±–∫–∞: –ü–æ—Ä—Ç ${PORT} —É–∂–µ –∏—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è!`);
        console.error(`   –ó–∞–∫—Ä–æ–π—Ç–µ –¥—Ä—É–≥–æ–µ –ø—Ä–∏–ª–æ–∂–µ–Ω–∏–µ –∏–ª–∏ –∏–∑–º–µ–Ω–∏—Ç–µ –ø–æ—Ä—Ç.`);
    } else {
        console.error('‚ùå –û—à–∏–±–∫–∞ —Å–µ—Ä–≤–µ—Ä–∞:', err.message);
    }
    process.exit(1); // –ó–∞–≤–µ—Ä—à–∞–µ–º –ø—Ä–æ—Ü–µ—Å—Å —Å –∫–æ–¥–æ–º –æ—à–∏–±–∫–∏
});
