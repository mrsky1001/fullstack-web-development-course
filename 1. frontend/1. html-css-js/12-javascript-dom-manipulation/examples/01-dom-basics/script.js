let itemCount = 2;

function addItem() {
    itemCount++;
    const list = document.getElementById('item-list');

    // 1. –°–æ–∑–¥–∞—ë–º –Ω–æ–≤—ã–π —ç–ª–µ–º–µ–Ω—Ç
    const newItem = document.createElement('li');

    // 2. –î–æ–±–∞–≤–ª—è–µ–º —Å–æ–¥–µ—Ä–∂–∏–º–æ–µ
    newItem.textContent = `–≠–ª–µ–º–µ–Ω—Ç ${itemCount}`;

    // 3. –ú–æ–∂–Ω–æ –¥–æ–±–∞–≤–∏—Ç—å –∫–ª–∞—Å—Å—ã –∏ –∞—Ç—Ä–∏–±—É—Ç—ã
    newItem.classList.add('list-item');
    newItem.setAttribute('data-id', itemCount);

    // 4. –î–æ–±–∞–≤–ª—è–µ–º –≤ DOM
    list.appendChild(newItem);

    // –î–æ–±–∞–≤–ª—è–µ–º –∞–Ω–∏–º–∞—Ü–∏—é –ø–æ—è–≤–ª–µ–Ω–∏—è
    newItem.style.animation = 'fadeIn 0.3s ease';
}

function removeLastItem() {
    const list = document.getElementById('item-list');
    const lastItem = list.lastElementChild;

    // –ü—Ä–æ–≤–µ—Ä—è–µ–º, –µ—Å—Ç—å –ª–∏ —ç–ª–µ–º–µ–Ω—Ç—ã –¥–ª—è —É–¥–∞–ª–µ–Ω–∏—è
    if (lastItem) {
        // –°–æ–≤—Ä–µ–º–µ–Ω–Ω—ã–π —Å–ø–æ—Å–æ–± —É–¥–∞–ª–µ–Ω–∏—è
        lastItem.remove();
        itemCount--;
    } else {
        console.log('–°–ø–∏—Å–æ–∫ –ø—É—Å—Ç!');
    }
}

