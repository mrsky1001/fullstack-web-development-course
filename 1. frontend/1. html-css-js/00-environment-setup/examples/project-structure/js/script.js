/* ================================================
   JAVASCRIPT — ДОБАВЛЕНИЕ ИНТЕРАКТИВНОСТИ
   ================================================
   
   JavaScript — язык программирования для веб-страниц.
   Он позволяет:
   - Реагировать на действия пользователя (клики, ввод)
   - Изменять содержимое страницы динамически
   - Отправлять и получать данные с сервера
   
   Этот файл демонстрирует базовые возможности JavaScript.
*/

// ------------------------------------------------
// ВЫВОД В КОНСОЛЬ
// ------------------------------------------------
// console.log() — функция для вывода сообщений в консоль браузера.
// Открыть консоль: F12 → вкладка Console
// Используется для отладки и проверки работы кода.

console.log('Скрипт загружен успешно!');

// ------------------------------------------------
// РАБОТА С ЭЛЕМЕНТАМИ СТРАНИЦЫ (DOM)
// ------------------------------------------------
// DOM (Document Object Model) — объектная модель документа.
// JavaScript может находить элементы на странице и изменять их.

// getElementById() — находит элемент по его атрибуту id
// В нашем HTML есть <div id="message"></div>
const messageElement = document.getElementById('message');

// ------------------------------------------------
// ИЗМЕНЕНИЕ СОДЕРЖИМОГО ЭЛЕМЕНТА
// ------------------------------------------------
// textContent — свойство для установки текстового содержимого

// Проверяем, что элемент найден (хорошая практика!)
if (messageElement) {
    // Устанавливаем текст внутри элемента
    messageElement.textContent = '✅ JavaScript работает! Окружение настроено правильно.';

    // Выводим подтверждение в консоль
    console.log('Сообщение успешно добавлено на страницу');
}

// ------------------------------------------------
// ПЕРЕМЕННЫЕ И ТИПЫ ДАННЫХ (ПРЕВЬЮ)
// ------------------------------------------------
// Это лишь превью — подробно изучим в уроке по JavaScript

// const — константа, значение нельзя изменить
const siteName = 'Мой первый сайт';

// let — переменная, значение можно изменить
let visitCount = 1;

// Выводим информацию в консоль
console.log('Название сайта:', siteName);
console.log('Номер посещения:', visitCount);

// ------------------------------------------------
// ДАТА И ВРЕМЯ
// ------------------------------------------------
// Date — встроенный объект для работы с датой и временем

const now = new Date();
console.log('Текущая дата и время:', now.toLocaleString('ru-RU'));

// ------------------------------------------------
// ИТОГ
// ------------------------------------------------
console.log('---');
console.log('Если вы видите эти сообщения, JavaScript работает корректно!');
console.log('Переходите к изучению следующих уроков.');
